    configs = {}
    
    # Config principal
    config_path = Path("tenants/timmy_vendas/config.json")
    if config_path.exists():
        with open(config_path, 'r', encoding='utf-8') as f:
            configs['config'] = json.load(f)
        print("   ‚úÖ config.json carregado")
    else:
        print("   ‚ùå config.json n√£o encontrado")
        configs['config'] = {}
    
    # Knowledge base
    knowledge_path = Path("tenants/timmy_vendas/knowledge.json")
    if knowledge_path.exists():
        with open(knowledge_path, 'r', encoding='utf-8') as f:
            configs['knowledge'] = json.load(f)
        print("   ‚úÖ knowledge.json carregado")
    else:
        print("   ‚ùå knowledge.json n√£o encontrado")
        configs['knowledge'] = {}
    
    # Target config
    target_path = Path("tenants/timmy_vendas/target_config.json")
    if target_path.exists():
        with open(target_path, 'r', encoding='utf-8') as f:
            configs['target'] = json.load(f)
        print("   ‚úÖ target_config.json carregado")
    else:
        print("   ‚ùå target_config.json n√£o encontrado")
        configs['target'] = {}
    
    # Examples
    examples_path = Path("tenants/timmy_vendas/examples.jsonl")
    if examples_path.exists():
        configs['examples'] = []
        with open(examples_path, 'r', encoding='utf-8') as f:
            for line in f:
                if line.strip():
                    configs['examples'].append(json.loads(line))
        print(f"   ‚úÖ examples.jsonl carregado ({len(configs['examples'])} exemplos)")
    else:
        print("   ‚ùå examples.jsonl n√£o encontrado")
        configs['examples'] = []
    
    return configs

