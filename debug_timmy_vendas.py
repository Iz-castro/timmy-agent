def analyze_target_system(target_config):
    """Analisa o sistema de captura de target"""
    
    if not target_config:
        print("   ‚ùå ERRO: Configura√ß√£o de target n√£o encontrada!")
        return
    
    print(f"   üéØ Target: {target_config.get('target_name', 'N/A')}")
    print(f"   üìù Descri√ß√£o: {target_config.get('description', 'N/A')}")
    print(f"   üîÑ Auto captura: {target_config.get('auto_capture', False)}")
    
    fields = target_config.get('fields', [])
    print(f"\n   üìã CAMPOS CONFIGURADOS ({len(fields)} total):")
    
    required_fields = []
    optional_fields = []
    
    for field in fields:
        field_name = field.get('field_name', 'N/A')
        display_name = field.get('display_name', 'N/A')
        field_type = field.get('field_type', 'N/A')
        required = field.get('required', False)
        choices = field.get('choices', [])
        triggers = field.get('prompt_triggers', [])
        
        if required:
            required_fields.append(field_name)
            status = "‚≠ê OBRIGAT√ìRIO"
        else:
            optional_fields.append(field_name)
            status = "üîµ Opcional"
        
        print(f"\n      üè∑Ô∏è {display_name} ({field_name})")
        print(f"         Tipo: {field_type} | {status}")
        
        if choices:
            print(f"         Op√ß√µes: {', '.join(choices[:3])}{'...' if len(choices) > 3 else ''}")
        
        if triggers:
            print(f"         Triggers: {', '.join(triggers[:3])}{'...' if len(triggers) > 3 else ''}")
    
    print(f"\n   üìä RESUMO DA CAPTURA:")
    print(f"      ‚Ä¢ Campos obrigat√≥rios: {len(required_fields)} ({', '.join(required_fields)})")
    print(f"      ‚Ä¢ Campos opcionais: {len(optional_fields)} ({', '.join(optional_fields)})")
    
    completion_triggers = target_config.get('completion_triggers', [])
    if completion_triggers:
        print(f"      ‚Ä¢ Triggers de finaliza√ß√£o: {', '.join(completion_triggers)}")

